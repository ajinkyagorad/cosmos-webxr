const LY_PER_PC = 3.26156;

/** Distance in parsecs for a catalog object name, or null when unknown. */
export function dsoDistancePc(name: string): number | null {
  const ly = DSO_DISTANCE_LY[normalizeDSOKey(name)];
  return ly == null ? null : ly / LY_PER_PC;
}

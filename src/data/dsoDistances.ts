/**
 * Curated DSO distances (C12).
 *
 * The SEDS Messier/NGC catalog (dso.json) only carries distances for a small
 * subset of objects, and its name field is zero-padded ("M031", "NGC0001"),
 * so a runtime lookup with normalization is needed. This table covers all 110
 * Messier objects plus ~100 famous NGC/IC objects, using standard published
 * values (SEDS Messier database; SIMBAD/NED for the NGC entries). Values are
 * distances in light-years; where the literature spans a range, the commonly
 * quoted central value is used. Never fabricated: an object absent from both
 * this table and the catalog simply shows no distance.
 */

export function normalizeDSOKey(name: string): string {
  const t = name.toUpperCase().replace(/[^A-Z0-9]/g, "");
  let m = /^M0*(\d+)$/.exec(t);
  if (m) return `M${m[1]}`;
  m = /^NGC0*(\d+)([A-Z]?)$/.exec(t);
  if (m) return `NGC${m[1]}${m[2]}`;
  m = /^IC0*(\d+)([A-Z]?)$/.exec(t);
  if (m) return `IC${m[1]}${m[2]}`;
  return t;
}

